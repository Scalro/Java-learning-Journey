package primitiveDataTypes;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		int num = 350000;
		char letter = 'A';
		byte n = 8;
		float dec = 234.23456f;
		double doub = 3456.456784567d;
		boolean check = true;
		long longInt = 234567899;
		
		System.out.println("Integer: " + num);
		System.out.println("Character: " + letter);
		System.out.println("Byte: " + n);
		System.out.println("Float: " + dec);
		System.out.println("Double: " + doub);
		System.out.println("Boolean: " + check);
		System.out.println("Long Integer: " + longInt);
	}

}
