package logicalOperators;

public class LogicalOperators {

	public static void main(String[] args) {
		
		int val1 = 9;
		int val2 = 11;
		
		if ((val1 == 9) && (val2 == 11))
			System.out.println("val1 == 9 AND val2 == 11");
		
		if ((val1 == 9) || (val2 == 10))
			System.out.println("val1 == 9 OR val2 == 11");

	}

}
