package unaryOperators;

public class UnaryOperators {

	public static void main(String[] args) {
		
		int i = +5;
		
		i = -5;
		System.out.println(i);
		
		i = +5;
		System.out.println(i);
		
		i++;
		System.out.println(i);
		
		i--;
		System.out.println(i);
		
		++i;
		System.out.println(i);
		
		--i;
		System.out.println(i);
		
		System.out.println(i++);
		
		System.out.println(++i);
		
		boolean result = false;
		System.out.println(!result);
		
		
		

	}

}
