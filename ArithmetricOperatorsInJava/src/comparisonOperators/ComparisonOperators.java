package comparisonOperators;

public class ComparisonOperators {

	public static void main(String[] args) {
		
		int val1 = 5;
		int val2 = 7;
		
		
		if (val1 == val2)
			System.out.println("val1 == val2");
		
		if (val1 < val2)
			System.out.println("val1 < val2");
		
		if (val1 > val2)
			System.out.println("val1 > val2");
		
		if (val1 <= val2)
			System.out.println("val1 <= val2");
		
		if (val1 >= val2)
			System.out.println("val1 >= val2");
		
		if (val1 != val2)
			System.out.println("val1 != val2");

	}

}
